async function seedCities() {
  try {
    await mongoose.connect(process.env.MONGODB_URI || 'mongodb://localhost:27017/flyticket');
    console.log('Connected to MongoDB');

    for (const cityName of turkishCities) {
      const exists = await City.findOne({ city_name: cityName });
      if (!exists) {
        await City.create({ city_name: cityName });
        console.log(`Inserted city: ${cityName}`);
      } else {
        console.log(`City already exists: ${cityName}`);
      }
    }

    console.log('City seeding completed.');
    process.exit(0);
  } catch (error) {
    console.error('Error seeding cities:', error);
    process.exit(1);
  }
}

